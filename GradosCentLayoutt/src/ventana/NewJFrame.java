    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            int n = 0;
            
            try{
            n = Integer.parseInt(jTextField1.getText());
            } catch (NumberFormatException e){
                //JOptionPane.showMessageDialog(this, "Solo numeros");
                JOptionPane.showMessageDialog(this, "Solo numeros", "error", JOptionPane.ERROR_MESSAGE);
                //JOptionPane.showMessageDialog(this, "Solo numeros", "error", JOptionPane.WARNING_MESSAGE);
            }
            
            if (jRadioButton1.isSelected() == true){
                n = n * n;
                //JOptionPane.showMessageDialog(this, "mensaje");
            }
           else{
            n = n * n * n;
            
        }
        jTextField2.setText(Integer.toString(n));
    }//GEN-LAST:event_jButton1ActionPerformed

