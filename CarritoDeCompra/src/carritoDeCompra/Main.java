    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        model = (DefaultTableModel) jTableCarrito.getModel();
        int fila = jTableCarrito.getSelectedRow();
        
        if (fila != -1){
            model.removeRow(fila);
            totalAPagar = calcularTotalAPagar(model);
            jTextTotalPagar.setText(String.format("%.2f", totalAPagar));
        }else{
            JOptionPane.showMessageDialog(null, "Selecciona el producto a eliminar");
        }        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombreProducto = jTextNombreProducto.getText().trim();
        String unidadMedida = jComboBoxUnidad.getSelectedItem().toString();
        boolean incluyeIVA = jCheckBoxIVA.isSelected();
        String cantidadTexto = jTextCantidad.getText().trim();
        String costoTexto = jTextCosto.getText().trim();

        if (!nombreProducto.matches("^[A-Za-z ]+$")) {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre de producto v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (cantidadTexto.isEmpty() || costoTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos antes de agregar el producto al carrito", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int cantidad;
        try {
            cantidad = Integer.parseInt(cantidadTexto);
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser un valor real √≥ mayor a 0", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor num√©rico real.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double costo;
        try {
            costo = Double.parseDouble(costoTexto);
            if (costo <= 0) {
                JOptionPane.showMessageDialog(this, "El costo debe ser un valor real √≥ mayor a 0", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor num√©rico real", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double iva = 0;
        
        if(incluyeIVA){
            iva = costo * 0.16 * cantidad;
        }
        
        double subtotal = costo * cantidad;

        DefaultTableModel model = (DefaultTableModel) jTableCarrito.getModel();
        model.addRow(new Object[]{nombreProducto, unidadMedida, iva, cantidad, costo, subtotal});

        totalAPagar = calcularTotalAPagar(model);
        jTextTotalPagar.setText(String.format("%.2f", totalAPagar));

        jTextNombreProducto.setText("");
        jComboBoxUnidad.setSelectedIndex(0);
        jCheckBoxIVA.setSelected(false);
        jTextCantidad.setText("");
        jTextCosto.setText("");
    }
    
    private double calcularTotalAPagar(DefaultTableModel model){
        double total = 0;
        for (int fila = 0; fila < model.getRowCount(); fila++){
            double subtotal = (double) model.getValueAt(fila, 5);
            total += subtotal;
        }
        return total;
    }
    
    private void eliminarProducto() {
        int filaEliminar = jTableCarrito.getSelectedRow();
        if (filaEliminar != -1) {
            model.removeRow(filaEliminar);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

